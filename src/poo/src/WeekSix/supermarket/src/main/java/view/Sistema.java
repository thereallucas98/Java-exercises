    // Clear Inputs
    
    public void clearInputs() {
        this.codeInsert.setText("");
        this.nameInsert.setText("");
        this.valueInsert.setText("");
        this.quantityInsert.setText("");
    }
    
    // add an Product
        
    
    public void addProduct() {
        
        int codeToBeInsert = Integer.parseInt(this.codeInsert.getText());
        int quantityToBeInsert = Integer.parseInt(this.quantityInsert.getText());
        float valueToBeInsert = Float.parseFloat(this.valueInsert.getText());
        String descriptionToBeInsert = this.nameInsert.getText();
        
        itemToBeAdd.addItem(codeToBeInsert, quantityToBeInsert, valueToBeInsert, descriptionToBeInsert);
        itens.add(itemToBeAdd);
        
        this.listShopping();
        this.clearInputs();
    }
    
    public void listShopping() {
        String resultList = itemToBeAdd.getAllItens();
        showItens.setText(resultList);
    }
    
    public void endShopping() {
        
        String s = String.valueOf(itemToBeAdd.getTotal());
        
        resultShopping.setText("R$" + s);
    }

